#include <bits/stdc++.h>

using namespace std;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(NULL);

    int q;

    cin >> q;

    while (q--) {
        int n;
        char ch;

        cin >> n >> ch;

        for (int i {}; i < n; ++i) {
            cout << ch;
        }

        cout << '\n';
    }
}